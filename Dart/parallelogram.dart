double calculateParallelogramArea(double base, double height) {
  return base * height;
}

void main() {
  double base = 10.0; // Replace with the actual base of the parallelogram
  double height = 4.0; // Replace with the actual height of the parallelogram
  double area = calculateParallelogramArea(base, height);
  print('The area of the parallelogram is $area');
}
